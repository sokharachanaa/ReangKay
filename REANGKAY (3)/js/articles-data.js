/* ---------- Helper: convert an ISO date (YYYY-MM-DD) to a Khmer-style date string ---------- */
const KHMER_MONTHS = ["មករា","កុម្ភៈ","មីនា","មេសា","ឧសភា","មិថុនា","កក្កដា","សីហា","កញ្ញា","តុលា","វិច្ឆិកា","ធ្នូ"];
const KHMER_DIGITS = ["០","១","២","៣","៤","៥","៦","៧","៨","៩"];

function toKhmerDigits(num){
  return String(num).split('').map(ch => (/[0-9]/.test(ch) ? KHMER_DIGITS[Number(ch)] : ch)).join('');
}

function formatKhmerDate(isoDateStr){
  const d = new Date(isoDateStr + 'T00:00:00');
  if (isNaN(d)) return '';
  const day = toKhmerDigits(d.getDate());
  const month = KHMER_MONTHS[d.getMonth()];
  const year = toKhmerDigits(d.getFullYear());
  return `${day} ${month} ${year}`;
}

/* ---------- Helper: estimate reading time from the article content ---------- */
function estimateReadMinutes(article){
  const words = (article.content || article.description || '').replace(/<[^>]*>/g, ' ').trim().split(/\s+/).filter(Boolean).length;
  return Math.max(1, Math.round(words / 130));
}

/* ---------- Helper: look up a single article by id (works with string or number ids) ---------- */
function getArticleById(id){
  return healthArticles.find(a => String(a.id) === String(id));
}

/* ---------- Helper: pick related articles — same category first, then anything else ---------- */
function getRelatedArticles(currentId, count = 3){
  const current = getArticleById(currentId);
  const others = healthArticles.filter(a => String(a.id) !== String(currentId));
  if (!current) return others.slice(0, count);

  const sameCategory = others.filter(a => a.category === current.category);
  const rest = others.filter(a => a.category !== current.category);
  return [...sameCategory, ...rest].slice(0, count);
}

